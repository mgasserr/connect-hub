    private void unsendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unsendButtonActionPerformed
        errorText.setText("");
        int i = usersList.getSelectedIndex();
        if (i == -1) {
            errorText.setForeground(Color.red);
            errorText.setText("No users selected");
        } else {
            Database.read();
            notiDatabase.read();
            acc.getFriendsManagement().unsendFriendRequest(usersList.getSelectedValue(), acc.getUsername());
            Database.getAccount(usersList.getSelectedValue()).removeReqNotibySender(acc.getUsername());
            Database.save();
            notiDatabase.save();
            listModel.clear();
            for (Account user : Database.getSentReqsDATABASE(acc.getUsername())) {
                listModel.addElement(user.getUsername());
            }
            usersList.setModel(listModel);
            errorText.setForeground(Color.black);
            errorText.setText("Friend request unsent!");
        }
    }//GEN-LAST:event_unsendButtonActionPerformed

